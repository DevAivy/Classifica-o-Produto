package HoraDeCodar.Variaveis;

public class Variavels {

    public static void main(String[] args) {
        String nome = "David";
        System.out.println(nome);

        String sobrenome = "Reis";
        System.out.println(sobrenome);

        String Curso = "Software engineer";
        System.out.println("I am: " + Curso);

        int idade = 18;
        System.out.println(idade);

        char letra = 'A';
        System.out.println(letra);

        long populaçãoMundial = 8000000000L;
        System.out.println(populaçãoMundial);

        double cocacola = 9.50;
        double desconto = cocacola - 3.50;
        System.out.println("R$" + cocacola);
        System.out.println("Com desconto de 3 reais fica: " + desconto);


    }
}
