package HoraDeCodar.Condicionais;

// == (igual a) Verifica se dois valores são iguais
// != (diferente de) Verifica se dois valores sao diferentes
// > (maior que) Verifica se um valor a esquerda é maior do que um valor a direita
// < (menor que) Verifica se um valor a esquerda é menor do que um valor a direita
// >= (maior ou igual) Verifica se um valor a esquerda é maior ou igual a um valor a direia
// <= (menor ou igual) Verifica se um valor a esquerda é menor ou igual a um valor a direia

public class OpComparacao {

    public static void main(String[] args) {

        int a = 10;
        System.out.println(a == 10);

        int b = 11;
        System.out.println(b != 12);

        int c = 12;
        System.out.println(c > 10);

        int d = 13;
        System.out.println(d > 3);

        int e = 14;
        System.out.println(e >= 12);

        int f = 15;
        System.out.println(f <= 18);

    }
}
