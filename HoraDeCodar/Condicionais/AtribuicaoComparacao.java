package HoraDeCodar.Condicionais;

// Atribuição (=);
//                 Atribui um valor a uma variável
//                 Usado para definir ou alterar o valor armanezado na variável
//                 Exemplo: int a = 5
//
// Comparação (==);
//                 Compara dois valores para ver se são iguais
//                 Retorna true se os valores forem iguais e false se forem diferentes
//                 Exemplo: int 5 == 5 retorna true;

public class AtribuicaoComparacao {

    public static void main(String[] args) {

        int valor = 35;
        System.out.println(valor = 50);
        System.out.println(valor == 35);

    }
}
